% equilibrium plots

clear all;
close all;

directory = '../../data/Ge/10_solar/10_solar_water/';
fid = fopen(strcat(directory,'Saturn_1.plt'),'r');
% specify number of t,p+species
n_column = 17;

if fid>=0
   fscanf(fid,'%s',1);
   s = cell(n_column);
   for i=1:n_column
       s{i} = fscanf(fid,'%s',1);
   end
   data_1 = fscanf(fid,'%f');
   data_1 = reshape(data_1,n_column,100);
   data_1 = data_1';
   data_short_1 = data_1(1:11:end,:);
end
fclose(fid);

fid = fopen(strcat(directory,'Saturn_2.plt'),'r');
if fid>=0
   fscanf(fid,'%s',1);
   s = cell(n_column);
   for i=1:n_column
       s{i} = fscanf(fid,'%s',1);
   end
   data_2 = fscanf(fid,'%f');
   data_2 = reshape(data_2,n_column,100);
   data_2 = data_2';
   data_short_2 = data_2(1:11:end,:);
end
fclose(fid);

fid = fopen(strcat(directory,'Saturn_3.plt'),'r');
if fid>=0
   fscanf(fid,'%s',1);
   s = cell(n_column);
   for i=1:n_column
       s{i} = fscanf(fid,'%s',1);
   end
   data_3 = fscanf(fid,'%f');
   data_3 = reshape(data_3,n_column,25);
   data_3 = data_3';
   data_short_3 = data_3(1:6:end,:);
end
fclose(fid);

data_short = [data_short_1;data_short_2;data_short_3];

set(gca,'FontSize',n_column);
T = data_short(:,1);
h1 = plot(data_short(:,3),T,'b-','LineWidth',2);
xlim([1e-20,1e-7])
ylim([300,1500])
set(gca,'xscale','log')
xlabel('mole fraction','interpreter','latex')
ylabel('T(K)','interpreter','latex')
grid on;

hold on;
for i=4:n_column
    plot(data_short(:,i),T,'b-','LineWidth',2);
end

